    c.first_name,
    c.last_name,
    c.email
FROM 
    customers c
JOIN 
    orders o ON c.customer_id = o.customer_id
JOIN 
    products p ON o.product_id = p.product_id
WHERE 
    p.unit_price > 50
GROUP BY 
    c.customer_id;

